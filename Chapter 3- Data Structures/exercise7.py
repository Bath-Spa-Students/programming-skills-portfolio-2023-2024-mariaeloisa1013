#store five places in the world id visit in a list unalphabetically
places = ["Japan", "USA", "Philippines", "Canada", "Paris"]

#raw order
print("\nOG order: ")
print(places)

#alphabetically
print ("\nAlphabetically: ")
print(sorted(places))

#show normal list order
print("\nOG order: ")
print(places)

#reverse
print ("\nReverse: ")
places.reverse()
print(places)

#reverse the reversed list
print ("\nReverse again: ")
places.reverse()
print(places)

#sort to alphabetical
print ("\nAlphabetical again: ")
places.sort()
print(places)

#reversed alphabetical
print("\nReverse alphabet:")
places.sort
places.reverse()
print(places)


